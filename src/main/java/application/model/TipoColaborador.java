package application.model;

/**
 * Enum que define os tipos de colaborador no sistema.
 * MOTORISTA = colaborador que conduz carros
 * ADMINISTRATIVO = funções internas
 * GESTOR = funções de gestão
 * OUTRO = qualquer outro tipo
 */
public enum TipoColaborador {
    MOTORISTA,
    ADMINISTRATIVO,
    GESTOR,
    OUTRO
}
